#!/usr/bin/env python3
"""
This module defines a function add which takes two floats and returns their sum.
"""

def add(a: float, b: float) -> float:
    """
    Adds two floats and returns the sum.

    Args:
        a (float): The first float value.
        b (float): The second float value.

    Returns:
        float: The sum of a and b.
    """
    return a + b
